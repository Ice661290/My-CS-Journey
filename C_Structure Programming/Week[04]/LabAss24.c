#include <stdio.h>

int main() {
    int i = 1;
    int sum = 0;

    do {
        sum += i;
        i++;
    } while (i <= 10); // ตรวจสอบเงื่อนไขตอนจบ (อย่าลืม ;)

    printf("Sum 1 to 10 is %d\n", sum);
    return 0;
}